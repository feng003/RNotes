class Shuffel
{
    public static void main(String[] args) {
        int x = 3;
        while (x > 0) {
            if(x > 2)
            {
                System.out.println();
            }
            x = x - 1;
            System.out.println();
            if(x == 2)
            {
                System.out.println();
            }
            if (x == 1) {
                System.out.println();
            }
        }
    }
}
